DECLARE
    -- table list, variable for table's name
    table_names SYS.odcivarchar2list := SYS.odcivarchar2list('manager', 'delivery_man', 'customer', 'delivery', 'board_qna', 'board_notice', 'address_area');
    table_name VARCHAR2(20);
    
    -- sequence list, variable for sequence's name
    sequence_names SYS.odcivarchar2list := SYS.odcivarchar2list('DELIVERY_MAN_SEQ', 'seq_customer_id', 'seq_delivery_no', 'BOARD_QNA_SEQ', 'seq_board_notice_no');
    sequence_name VARCHAR2(20);
BEGIN
    -- delete tables
    FOR i IN 1..table_names.COUNT LOOP
        table_name := table_names(i);
        
        BEGIN
            EXECUTE IMMEDIATE 'DROP TABLE ' || table_name || ' CASCADE CONSTRAINTS';
        EXCEPTION
            WHEN OTHERS THEN
                NULL;
        END;
    END LOOP;

    -- delete sequences
    FOR i IN 1..sequence_names.COUNT LOOP
        sequence_name := sequence_names(i);
        
        BEGIN
            EXECUTE IMMEDIATE 'DROP SEQUENCE ' || sequence_name;
        EXCEPTION
            WHEN OTHERS THEN
                NULL;
        END;
    END LOOP;
END;
/






-- create tables
CREATE TABLE manager(
    id      VARCHAR2(20)   PRIMARY KEY,
    pw      VARCHAR2(20)   NOT NULL,
    name    VARCHAR2(15)   NOT NULL,
    tel     VARCHAR2(11)   NOT NULL
);

CREATE TABLE delivery_man(
    id              CHAR(10)        PRIMARY KEY,
    pw             VARCHAR(20)     NOT NULL,
    join_date       DATE            DEFAULT SYSDATE     NOT NULL,
    status          NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(status IN(0, 1, 2)),
    name            VARCHAR2(15)    NOT NULL,
    tel             VARCHAR2(11)    NOT NULL,
    post_code       CHAR(6)         NOT NULL,
    road_address    VARCHAR2(150)   NOT NULL,
    detail_address  VARCHAR2(150),
    area1           VARCHAR2(30)    NOT NULL,
    area2           VARCHAR2(30)    NOT NULL,
    area3           VARCHAR2(30),
    expired_date    DATE
);

CREATE TABLE customer(
    id              CHAR(10)        PRIMARY KEY,
    join_date       DATE            DEFAULT SYSDATE     NOT NULL,
    status          NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(status IN (0, 1)),
    name            VARCHAR2(15)    NOT NULL,
    tel             VARCHAR2(11)    NOT NULL,
    post_code       CHAR(6)         NOT NULL,
    road_address    VARCHAR2(150)   NOT NULL,
    detail_address  VARCHAR2(150),
    area3           VARCHAR2(30),
    delivery_man_id CHAR(10)        NOT NULL            REFERENCES delivery_man(id),
    leave_date      DATE
);

CREATE TABLE delivery(
    no              CHAR(14)        PRIMARY KEY,
    due_date        DATE            NOT NULL,
    customer_id     CHAR(10)        NOT NULL            REFERENCES customer(id),
    delivery_man_id CHAR(10)        NOT NULL            REFERENCES delivery_man(id),
    status          NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(status IN (0, 1)),
    customer_status NUMBER(1)       CHECK(customer_status IN (0, 1, 2))
);

CREATE TABLE board_qna(
    no          NUMBER          PRIMARY KEY,
    title       VARCHAR2(90)    NOT NULL,
    q_content   CLOB            NOT NULL,
    q_author    CHAR(10)        NOT NULL            REFERENCES delivery_man(id),
    q_date      DATE            DEFAULT SYSDATE     NOT NULL,
    status      NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(status IN (0, 1)),
    a_author    VARCHAR2(20)    REFERENCES manager(id),
    a_Date      DATE,
    a_content   CLOB,
    is_deleted  NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(is_deleted IN (0, 1))
);

CREATE TABLE board_notice(
    no          NUMBER          PRIMARY KEY,
    title       VARCHAR2(150)   NOT NULL,
    content     CLOB            NOT NULL,
    author      VARCHAR2(20)    NOT NULL            REFERENCES manager(id),
    post_date   DATE            DEFAULT SYSDATE     NOT NULL,
    views       NUMBER          DEFAULT 0           NOT NULL,
    is_pinned   NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(is_pinned IN (0, 1)),
    is_hidden   NUMBER(1)       DEFAULT 0           NOT NULL    CHECK(is_hidden IN (0, 1))
);

CREATE TABLE address_area(
    code    CHAR(10)        PRIMARY KEY,
    area    VARCHAR2(30)    NOT NULL
);

-- create sequaneces
CREATE SEQUENCE DELIVERY_MAN_SEQ INCREMENT BY 1 MAXVALUE 9999999999999999999999999999 MINVALUE 1 CACHE 20;
CREATE SEQUENCE seq_customer_id INCREMENT BY 1 START WITH 1 MINVALUE 1 MAXVALUE 999 NOCYCLE NOCACHE;
CREATE SEQUENCE seq_delivery_no INCREMENT BY 1 START WITH 1 MINVALUE 1 MAXVALUE 99999 NOCYCLE NOCACHE;
CREATE SEQUENCE BOARD_QNA_SEQ INCREMENT BY 1 MAXVALUE 9999999999999999999999999999 MINVALUE 1 CACHE 20;
CREATE SEQUENCE seq_board_notice_no INCREMENT BY 1 START WITH 1 MINVALUE 1 NOMAXVALUE NOCYCLE NOCACHE;



